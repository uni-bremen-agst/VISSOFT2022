get_dist <- function(first, second) {
   transform(data.frame(first, second), count = ave(rep(1, nrow(data.frame(first, second))), first, second, FUN = seq_along))
}
violin <- function(data, label) {
    ggbetweenstats(data=data, x=x, y=y, xlab="Group", ylab=label, plot.type="boxviolin", type="np", package="ggsci", palette="uniform_startrek", ggtheme = theme_grey(base_size = SIZE), outlier.tagging = TRUE, outlier.label.args = list(size=6, color = "red", nudge_x=0.2), results.subtitle=F, point.args=list(size=5, alpha=0.4, position=position_jitter(w=0.1, h=0)), centrality.point.args=list(size=7, color="darkgreen", alpha=0.6), centrality.label.args=list(size=6, nudge_x=0.5, nudge_y=0.2, segment.linetype=1, min.segment.length=0)) + ourtheme + scale_y_continuous(breaks=pretty_breaks())
    #ggplot(data.frame(as.factor(group), as.numeric(data)), aes(x=as.factor(group), y=as.numeric(data))) + geom_violin(trim=T) + theme_grey(base_size = SIZE) + geom_boxplot(width=0.1, color="grey", alpha=0.4) + xlab("Group") + ylab(label)
}
bar <- function(data, label) {
    # NOTE: label currently unused
    ggplot(as.data.frame(table(data)), aes(x=data, y=Freq)) + geom_bar(stat="identity") + coord_flip() + labs(x = "", y="Count") + theme_grey(base_size = SIZE) + scale_y_continuous(breaks=pretty_breaks()) + ourtheme
}
report = function(data, name) {
    cat(name, "Average", mean(data), "Median", median(data), "Standard Deviation", sd(data), "\n")
}
